void main() {
  var x = -4;
  print(x.isOdd);
  print(x.isEven);
  print(x.isFinite);
  print(x.isInfinite);
  //NaN stands for Not a Number
  print(x.isNaN);
  print(x.isNegative);
  print(x.abs());
  var y = -8.5;
  print(y.round());
  print(y.floor());
  print(y.ceil());
  print(y.truncate());
  print(y.remainder(x));

}
