void main() {
  // main() is the starting point of any Dart program.
  // Execution always begins from here.
  // print() is used to display output in the console.
  print('any Text');

  // Strings in Dart can be written using:
  // Single quotes ' '
  // Double quotes " "
  // Triple quotes ''' ''' or """ """ for multi-line strings
  print('''Hello
  ddjkjk
  kjdkjdk
  'kdjkdjk'
  
  
   "World"  ''');
  
  print("""
  Hello 
  'World'
  """);

  // Dart evaluates arithmetic expressions before printing.
  // Here, 3 + 5 is calculated first, then the result (8) is printed.
  print(3 + 5);
  // When numbers are inside quotes, they are treated as text (String),
  print('3+5');
}
